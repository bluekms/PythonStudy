# ==============================
# list
#   append : 리스트 끝에 포인터 1개 추가
#   extend : 리스트 끝에 모든 원소 추가
#   copy : 깊은 복사
#   count : 해당 원소 수 반환
#   index : 리스트 값의 index 반환 (없으면 throw)
#   insert(a, b) : a위치에 b원소 삽입
#   pop : 지정한 위치의 원소를 꺼냄 (삭제됨)
#   remove : 원소 삭제 (처음 발견된것 하나)
# ==============================
x = [1, 2, 3, 4]
x.append([3, 4])
x.extend([3, 4])
y = x.copy()
x.count(3)
x.index(2)
x.insert(2, 999)
x.remove(2)


