set(zip(["a", "a", "c", "d"], [1, 2, 3, 4], [10, 20, 30, 40], "ABCD"))
set(zip(["a", "a", "c", "d"], [1, 1, 3, 4], [10, 10, 30, 40], "AACD"))


# ==============================
# min, max
# ==============================
x = {8: 1, 3: 9, -2: 1, 10: -1}
res = max(x)  # key가 가장 큰 요소의 key를 출력 (10)
res = max(x, key=lambda k: x[k])  # value가 가장 큰 요소의 key를 출력 (3)
x[res]  # value가 가장 큰 요소의 value를 출력


