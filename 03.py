def solution(대기인원):
    일년일수 = 0
    for i in range(10, 0, -1):
        일년일수 += 2 ** i
    년 = (대기인원 // 1200) // 일년일수
    남은일수 = (대기인원 // 1200) % 일년일수

    월별일수누적값 = 0
    월 = 0
    for i in range(10, 0, -1):
        차감일 = 월별일수누적값
        월별일수누적값 += 2 ** i
        월 += 1
        if 월별일수누적값 > 남은일수:
            break

    일 = 남은일수 - 차감일
    최종남은인원 = 대기인원 % 1200
    시 = 최종남은인원 // 100 + 9

    출발분 = [25, 40, 55, 70, 85, 100]
