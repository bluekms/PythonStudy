    for i in 출발분:
        if i > 해당시간에남은인원:
            분 = 출발분.index(i) * 10
            break

    if 최종남은인원 % 100 == 99:
        시 += 1
        분 = 0

    if 오늘시간.minute + 분 > 60:
        분 = (오늘시간.minute + 분) - 60
        시 += 1

    return f"{년 + 2020}년 {월}월 {일}일 {시}시 {분}분 출발"


